<div><span ><strong>LATAR BELAKANG
</strong></span></div>
<p><span >Dalam dunia informasi yang terus berkembang ini, para pelajar dituntut untuk tidak hanya mencari ilmu dan pengetahuan dari sekolah mereka, berbagai sumber ilmu lainnya telah tersedia dan menunggu untuk dimanfaatkan bagi mereka yang mengetahui caranya. Sebuah kelas tidak lagi terisolasi dengan kelas lainnya, melalui pemanfaatan ICT, setiap kelas dapat terhubung dengan kelas lainnya, memungkinkan terjadinya pertukaran informasi, berbagi pengalaman, dan belajar bersama dengan kelas lainnya.
</span></p>
<p><span >Kendala dalam pelaksanaan program kerjasama adalah sulitnya mencari partner yang memiliki konsentrasi ataupun minat yang sama dengan yang diminati. Tidak adanya sebuah wadah yang memungkinkan para guru atau murid untuk mencari partner yang dapat diajak untuk berdiskusi maupun berkolaborasi juga menjadi salah satu kendala. Sulitnya komunikasi antar kelas, serta kesulitan menentukan kegiatan yang dapat dilaksanakan bersama juga menjadi kendala dalam pelaksanaan program School Partnership.
</span></p>
<p><span >SEAMOLEC sebagai lembaga yang berfokus kepada pengembangan Pendidikan Jarak Jauh di Asea Tenggara melihat perlunya sebuah wadah yang dapat memfasilitasi kelas di Asia Tenggara untuk berpartner, berkolaborasi, berbagi. Kegiatan berbasis project akan dipilih guna meningkatkan aktifitas dan juga memberikan tantangan tersendiri bagi Siswa yang akan mengikuti kegiatan ini.
</span></p>
