<p style="margin-left: 18pt;">2.    Juara selanjutnya didapatkan di luar JUARA KONCER A, B dan C, yang diambil dari hasil rangking nominasi selanjutnya.</p>
<p style="margin-left: 18pt;">2    Apabila ada rangking yang sama disebabkan pengajuan nominasi yang sama misalnya masing mengajukan nominasi pada gantangan 5, 22, dan 10, maka urutan juara dimulai dari nomor gantangan terkecil</p>
