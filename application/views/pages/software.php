<strong>Software Bukan Menjadi Juri
</strong>

Software ini berangkat dari kondisi di lapangan lomba saat ini. Sistem ini bukan baru, yang baru adalah software aja, kami tidak merusak tatanan penjurian yang ada sekarang, Juri tetap bertugas seperti biasa.

Software bukanlah PENGADIL, PENGADIL tetap juri, karena semua diajukan dari dan untuk juri. Software hanya sebagai alat bantu juri untuk identifikasi burung-burung yang layak NOMINASI untuk diajukan ke JUARA KONCER.

<strong>KEUNGGULAN SOFTWARE NOMINASI
</strong>

Beberapa keungggulan Software ini adalah:
<p style="margin-left: 13pt;">1. Dapat melihat langsung ajuan nominasi terbanyak sampai terendah</p>
<p style="margin-left: 13pt;">2. Dapat melihat rangking nominasi secara otomatis</p>
<p style="margin-left: 13pt;">3. Dapat melihat ajuan nominasi yang sama jumlah ajuannya (Jumlah juri yg ajukan)</p>
<p style="margin-left: 13pt;">4. Dapat melihat ajuan juri terendah (Yang tidak layak juara, baik Juara koncer atau pun 10 besar)</p>
<p style="margin-left: 13pt;">5. Dapat Melihat nilai mentok langsung (38), Walau begitu Rekap MANUAL tetap kami butuhkan buat Double X Check saja.</p>
<p style="margin-left: 9pt;">6. Dengan software ini, juri tidak bisa main seenaknya untuk koncerin burung tertentu, karena kalau juri ingin mengajukan burung tertentu dan tidak ada ajuan lainnya, maka tidak akan menuju ke proses Juara koncer atau akan menjadi rangking terakhir di software nominasi.</p>
Apabila ada salah satu juri dimana ajuan nominasinya TIDAK MASUK ke TAHAP KONCER, maka si Juri tsb dikasih kesempatan untuk melihat burung-burung yang layak MAJU KONCER terlebih dahulu, dan selanjutnya dia wajib memebrikan bendera koncer pada pilihannya.

Software bisa dipakai oleh PBI, BnR atau Independent, dimana juri bisa mengajukan berapa ajuan nominasi misal 2, 3 atau berapa ... semakin banyak semakin perlu waktu, namun agar efektive sebaiknya yang paling layak juara lah yang diajukan oleh juri ke ajuan nominasi.

Disamping mempunyai keunggulan keemahan yang utama adalah apabila jumlah ajuan nominasi juri banyak, maka diperkukan waktu yg lama juga, makanya antisipasinya adalah Team Input harus mulai memasukan sata juri mendapatkan mulai 1 nominasi sampai terakhir dia dapatkan, sehingga waktu selesai input pun selesai dlakukan. Juga perlu sumber daya yang memadai terutama penguasaan komputer, antisipasi ini KM melibatkan team Admin sebagai Team IT Nominasi KM ini.

<strong>PENGAMBILAN JURI RANDOM
</strong>

Agar dihasilkan lomba yang bener-bener fair play dan se obyective mungkin, maka juri harus yang profesional dan diambil random, artinya juri diambil dari berbagai lokasi berbeda sehingga tidak ada istilah grup atau istilah trio dalam kelompok juri, dan di lapangan juri benar-benar diuji profesi dan pengalamannya dalam menilai lomba burung di KM yang menggunakan software nominasi karena pengajuan nominasi yang tidak sesuai kriteria penjurian akan otomatis menjadi rangking terendah.

Event KM berthema PEMILU KM CUP tg 22 Desember 2013 di Lapangan Banteng, maka kami akan mengambil juri dari berbagai propinsi: Jatim, Jogja, Jateng, Jabar dan Jabodetabek.

Setidaknya kami telah berusaha agar lomba benar-benar sesuai yang diharapkan semua pihak. AMIN

<strong>METODE PENENTUAN JUARA BERDASARKAN PENGAJUAN NOMINASI SESUAI KINERJA BURUNG
</strong>

Penentuan burung yang layak masuk nominasi ditentukan oleh Juri, setelah dilakukan minimal empat kali kontrolan di Lapangan, dimana burung-burung yang layak juara dimasukan ke dalam pengajuan nominasi.

Pengajuan nominasi tidak hanya dibatasi 3 saja, namun berdasarkan burung-burung yang kinerjanya bagus tapi layak masuk nominais. Namun untuk efektivitasnya waktu maka dapat ditentukan dengan tabel, lihat Tabel-2: Tabel Pengajuan Nominasi. Tabel ini hanya sebagai bantuan saja, dimana jumlah pengajuan dapat lebih atau malah kurang, tergantung kinerja dan performance burung di lapangan.
<p style="margin-left: 9pt;">•    Juri mengajukan burung-burung yang layak masuk nominasi kepada Team Perumus Nominasi sesuai burung yang mempunyai kinerja bagus (Setiap juri akan mempunyai ajuan berbeda)</p>
<p style="margin-left: 9pt;">•    Korlap, IP, dan Pengawas KM mengecek ajuan Juri, seumpama ada burung bagus kinerjanya akan tetapi belum masuk nominasi, dan diajukan minimal 2 orang Korlap, IP, Pengawas KM bisa diajukan ke Juri sesuai dengan banyaknya usulan dari Korlap, IP, Pengawas, kalau misal di catatan Korlap, IP, Pengawas KM hanya ada 4, maka dimasukan ke juri tsb dengan memberikan nilai mentok 38 kepada 4 juri yang bertugas, ini untuk menghindari nilai mentok yang kosong disebabkan oleh ajuan Korlap, IP, Pengawas KM karena yang mempunyai hak nilai mentok adalah juri.</p>
<p style="margin-left: 9pt;">•    Setelah Team Perumus Nominasi memasukan ke komputer, maka segera ditentukan urutan rangking dari 1 (jumlah pengajuan nominasi terbanyak) sampai dengan X (urutan jumlah pengajuan nominasi terkecil).</p>
<p style="margin-left: 9pt;">•    Team Perumus Nominasi memberikan data rangking Nominasi tertinggi, apabila rangking nominasi tertinggi hanya 1 burung bisa dikembangkan untuk diambil 1 s/d 2 atau 1 s/d 3 untuk menentukan Juara koncer A, B atau C.</p>
<strong>JUARA KONCER BENDERA A, B, dan C
</strong>

Penentuan Juara KONCER A, B dan atau C dapat dilakukan dengan cara sebagai berikut:
<p style="margin-left: 18pt;">1.     Juri mendapatkan data dari Team IT Nominasi lewat Korlap dan IP, dari hasil rangking nominasi</p>
<p style="margin-left: 18pt;">2.     Juri menentukan bendera koncer A, B, dan C berdasarkan pilihan masing-masing juri, berdasarkan AJUAN KONCER dari hasil rekap nominasi yang dilakukan oleh Team Nominasi KM.</p>
<p style="margin-left: 18pt;">3.    Alasan kenapa Juri bebas menentukan koncer terjadap pilihannya, karena apabila juri sudah ada pandangan yang bagus dari awal samai akhir, maka juri di pikirannya pasti akan mengoncerin burung tersebut.</p>
<p style="margin-left: 18pt;">4     Untuk menentukan juara 1, maka akan dilihat jumlah bendera A terbanyak, juara 2 dilihat jumlah bendera B terbanyak, dan seterusnya.</p>
<p style="margin-left: 18pt;">5.    Apabila ada juri dimana pengajuan nominasinya tidak masuk AJUAN KONCER, maka Juri tersebut WAJIB memantau terlebih dahulu burung yang masuk AJUAN KONCER, dan selanjutnya wajib memberikan koncer bendera A, B dan atau C pada burung tersebut (Tidak boleh keluar dari ajuan koncer). Ini untuk mengantisipasi bahwa juri tidak seenaknya mengajukan nominasi, namun akan tetap berdasarkan suara juri terbanyak (nilai mentok 38 tertinggi) untuk koncerin burung, cara ini cukup efektief antisipasi KKN.</p>
